public class Relational{
  public static void main(String args[]){
    int a = 10;
    int b = 5;
    System.out.println("For == operator: " + (a == b));
    System.out.println("For != operator: " + (a != b));
    System.out.println("For > operator: " + (a > b));
    System.out.println("For < operator: "+ (a < b));
  }
}